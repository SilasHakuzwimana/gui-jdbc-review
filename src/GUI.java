    public void loadTableData() {
        try {
            Connection conn = new Connector().getConnection();
            Statement stmt = conn.createStatement();
            String sql = "SELECT * FROM students";
            ResultSet rs = stmt.executeQuery(sql);
            DefaultTableModel tableModel = (DefaultTableModel) jTable2.getModel();

            // Clear table before reloading data
            tableModel.setRowCount(0);

            while (rs.next()) {
                int id = rs.getInt(1);
                String firstName = rs.getString(2);
                String lastName = rs.getString(3);
                String gender = rs.getString(4);
                tableModel.addRow(new Object[]{id, firstName, lastName, gender, null});
            }

            // Apply button renderer & editor AFTER loading data
            jTable2.getColumn("Actions").setCellRenderer(new ButtonRenderer());
            jTable2.getColumn("Actions").setCellEditor(new ButtonEditor(jTable2));

            rs.close();
            stmt.close();
            conn.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

