    private void registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerActionPerformed
        try {
            String fn = firstname.getText();
            String ln = lastname.getText().toUpperCase();
            int idv = Integer.parseInt(id.getText());
            String genderValue = gender.getSelectedItem().toString();
            Register register = new Register();
            long x = register.insert(idv, fn, ln, genderValue);
            if (x > 0) {
                JOptionPane.showMessageDialog(rootPane, "Successfully Added!!", "Result", JOptionPane.INFORMATION_MESSAGE);
                loadTableData();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Invalid Input", "Result", JOptionPane.WARNING_MESSAGE);
            id.setBackground(Color.red);
        } finally {
            id.setText("");
            firstname.setText("");
            lastname.setText("");
            gender.setSelectedIndex(0);

        }
    }//GEN-LAST:event_registerActionPerformed

